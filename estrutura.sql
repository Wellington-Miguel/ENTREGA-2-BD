-- Consulta 11
-- Requisito Associado: RE3 (Métricas de divulgação científica) e Requisito Original (Cadastrar pesquisadores).
-- Descrição: Identifica o pesquisador mais produtivo (com mais publicações) de cada área de laboratório.
--            Isso ajuda a reconhecer lideranças científicas em diferentes campos.
-- Complexidade: Usa 5 tabelas, SUB-QUERY (CTE), JOIN, COUNT, GROUP BY, e WINDOW.
WITH RankingPesquisadores AS (
    SELECT
        L.DS_AREA,
        P.NM_PESQUISADOR,
        COUNT(PA.CO_PUBLICACAO) AS "TotalPublicacoes",
        ROW_NUMBER() OVER(PARTITION BY L.DS_AREA ORDER BY COUNT(PA.CO_PUBLICACAO) DESC) as "Rank"
    FROM
        TB_PESQUISADOR P
    JOIN
        TB_PESQUISADOR_LABORATORIO PL ON P.ID_PESQUISADOR = PL.CO_PESQUISADOR
    JOIN
        TB_LABORATORIO L ON PL.CO_LABORATORIO = L.ID_LABORATORIO
    JOIN
        TB_PUBLICACAO_AUTORIA PA ON P.ID_PESQUISADOR = PA.CO_PESQUISADOR
    GROUP BY
        L.DS_AREA, P.NM_PESQUISADOR
)
SELECT
    DS_AREA,
    NM_PESQUISADOR,
    "TotalPublicacoes"
FROM
    RankingPesquisadores
WHERE
    "Rank" = 1;


-- Consulta 12
-- Requisito Associado: Original (Gerenciar distribuição de recursos financeiros) e RE3 (Métricas).
-- Descrição: Calcula o "custo por publicação" de cada projeto e, em seguida, a média desse custo por financiador.
--            Esta métrica avançada avalia a eficiência do investimento de cada entidade financiadora.
-- Complexidade: Usa 5 tabelas, SUB-QUERY (CTE), JOIN, COUNT, SUM, AVG, e GROUP BY.
WITH CustoPorProjeto AS (
    SELECT
        PR.ID_PROJETO,
        PR.NM_PROJETO,
        PR.VL_ORCAMENTO,
        COUNT(PA.CO_PUBLICACAO) AS "QtdPublicacoes"
    FROM
        TB_PROJETO PR
    LEFT JOIN
        TB_PESQUISADOR_PROJETO PP ON PR.ID_PROJETO = PP.CO_PROJETO
    LEFT JOIN
        TB_PUBLICACAO_AUTORIA PA ON PP.CO_PESQUISADOR = PA.CO_PESQUISADOR
    GROUP BY
        PR.ID_PROJETO, PR.NM_PROJETO, PR.VL_ORCAMENTO
)
SELECT
    F.NM_FINANCIADOR,
    AVG(CASE WHEN CPP."QtdPublicacoes" > 0 THEN CPP.VL_ORCAMENTO / CPP."QtdPublicacoes" ELSE NULL END) AS "CustoMedioPorPublicacao"
FROM
    TB_FINANCIADOR F
JOIN
    TB_FINANCIADOR_PROJETO FP ON F.ID_FINANCIADOR = FP.CO_FINANCIADOR
JOIN
    CustoPorProjeto CPP ON FP.CO_PROJETO = CPP.ID_PROJETO
GROUP BY
    F.NM_FINANCIADOR
HAVING
    AVG(CASE WHEN CPP."QtdPublicacoes" > 0 THEN CPP.VL_ORCAMENTO / CPP."QtdPublicacoes" ELSE NULL END) IS NOT NULL
ORDER BY
    "CustoMedioPorPublicacao" ASC;


-- Consulta 13
-- Requisito Associado: RE4 (Participação em eventos) e RE5 (Avaliar pesquisadores).
-- Descrição: Identifica pesquisadores cuja participação em eventos está acima da média geral e verifica
--            sua nota média de avaliação, para analisar se há correlação entre engajamento em eventos e desempenho.
-- Complexidade: Usa 4 tabelas, SUB-QUERY, JOIN, COUNT, AVG, e GROUP BY.
SELECT
    P.NM_PESQUISADOR,
    (SELECT COUNT(*) FROM TB_PESQUISADOR_EVENTO PE WHERE PE.CO_PESQUISADOR = P.ID_PESQUISADOR) AS "EventosParticipados",
    AVG(A.QT_NOTA) AS "NotaMediaAvaliacao"
FROM
    TB_PESQUISADOR P
JOIN
    TB_AVALIACAO A ON P.ID_PESQUISADOR = A.CO_PESQUISADOR
GROUP BY
    P.ID_PESQUISADOR, P.NM_PESQUISADOR
HAVING
    (SELECT COUNT(*) FROM TB_PESQUISADOR_EVENTO PE WHERE PE.CO_PESQUISADOR = P.ID_PESQUISADOR) >
    (SELECT AVG(TotalEventos) FROM (SELECT COUNT(CO_EVENTO) AS TotalEventos FROM TB_PESQUISADOR_EVENTO GROUP BY CO_PESQUISADOR) AS SubQuery)
ORDER BY
    "NotaMediaAvaliacao" DESC;


-- Consulta 14
-- Requisito Associado: RE1 (Gestão de Equipamentos) e RE3 (Métricas de divulgação científica).
-- Descrição: Analisa se laboratórios com mais equipamentos "de ponta" (ex: Servidores GPU, Sequenciadores)
--            possuem maior produção científica (nº de publicações).
-- Complexidade: Usa 5 tabelas, SUB-QUERY (CTE), JOIN, COUNT, e GROUP BY.
WITH MetricasLaboratorio AS (
    SELECT
        L.ID_LABORATORIO,
        L.NM_LABORATORIO,
        COUNT(DISTINCT PA.CO_PUBLICACAO) AS "TotalPublicacoes"
    FROM
        TB_LABORATORIO L
    JOIN
        TB_PESQUISADOR_LABORATORIO PL ON L.ID_LABORATORIO = PL.CO_LABORATORIO
    JOIN
        TB_PUBLICACAO_AUTORIA PA ON PL.CO_PESQUISADOR = PA.CO_PESQUISADOR
    GROUP BY
        L.ID_LABORATORIO, L.NM_LABORATORIO
)
SELECT
    ML.NM_LABORATORIO,
    ML."TotalPublicacoes",
    COUNT(E.ID_EQUIPAMENTO) AS "QtdEquipamentosDePonta"
FROM
    MetricasLaboratorio ML
JOIN
    TB_EQUIPAMENTO E ON ML.ID_LABORATORIO = E.CO_LABORATORIO
WHERE
    E.NM_EQUIPAMENTO LIKE '%GPU%' OR E.NM_EQUIPAMENTO LIKE '%Sequenciador%' OR E.NM_EQUIPAMENTO LIKE '%Supercomputador%'
GROUP BY
    ML.NM_LABORATORIO, ML."TotalPublicacoes"
ORDER BY
    "QtdEquipamentosDePonta" DESC, "TotalPublicacoes" DESC;


-- Consulta 15
-- Requisito Associado: Original (Monitorar projetos) e RE5 (Avaliar pesquisadores).
-- Descrição: Encontra pesquisadores "sobrecarregados" (em 3 ou mais projetos ativos) e compara a nota média
--            deles com a média de todos os pesquisadores avaliados, para insights sobre carga de trabalho e desempenho.
-- Complexidade: Usa 3 tabelas, SUB-QUERY, JOIN, COUNT, AVG, e GROUP BY.
SELECT
    P.NM_PESQUISADOR,
    COUNT(PP.CO_PROJETO) AS "ProjetosAtivos",
    AVG(A.QT_NOTA) AS "NotaMediaPessoal"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PESQUISADOR_PROJETO PP ON P.ID_PESQUISADOR = PP.CO_PESQUISADOR
JOIN
    TB_AVALIACAO A ON P.ID_PESQUISADOR = A.CO_PESQUISADOR
WHERE PP.CO_PROJETO IN (SELECT ID_PROJETO FROM TB_PROJETO WHERE ST_PROJETO = 'A')
GROUP BY
    P.NM_PESQUISADOR
HAVING
    COUNT(PP.CO_PROJETO) >= 3
ORDER BY
    "NotaMediaPessoal" DESC;


-- Consulta 16
-- Requisito Associado: RE3 (Métricas de divulgação científica).
-- Descrição: Mostra a evolução anual do número de publicações de cada pesquisador, usando uma função de janela
--            para calcular o total acumulado de publicações ao longo dos anos.
-- Complexidade: Usa 3 tabelas, SUB-QUERY (CTE), JOIN, COUNT, GROUP BY, e WINDOW.
WITH PublicacoesAnuais AS (
    SELECT
        P.NM_PESQUISADOR,
        EXTRACT(YEAR FROM PUB.DT_PUBLICACAO) AS "Ano",
        COUNT(PUB.ID_PUBLICACAO) AS "PublicacoesNoAno"
    FROM
        TB_PESQUISADOR P
    JOIN
        TB_PUBLICACAO_AUTORIA PA ON P.ID_PESQUISADOR = PA.CO_PESQUISADOR
    JOIN
        TB_PUBLICACAO PUB ON PA.CO_PUBLICACAO = PUB.ID_PUBLICACAO
    GROUP BY
        P.NM_PESQUISADOR, EXTRACT(YEAR FROM PUB.DT_PUBLICACAO)
)
SELECT
    NM_PESQUISADOR,
    "Ano",
    "PublicacoesNoAno",
    SUM("PublicacoesNoAno") OVER (PARTITION BY NM_PESQUISADOR ORDER BY "Ano") AS "TotalAcumulado"
FROM
    PublicacoesAnuais
ORDER BY
    NM_PESQUISADOR, "Ano";


-- Consulta 17
-- Requisito Associado: Original (Monitorar projetos) e RE3 (Métricas).
-- Descrição: Identifica projetos "de risco", definidos como aqueles com orçamento acima da média de todos os projetos,
--            mas com um número de publicações abaixo da média de publicações por projeto.
-- Complexidade: Usa 4 tabelas, SUB-QUERY, JOIN, COUNT, AVG, e GROUP BY.
SELECT
    PR.NM_PROJETO,
    PR.VL_ORCAMENTO,
    COUNT(PA.CO_PUBLICACAO) AS "QtdPublicacoes"
FROM
    TB_PROJETO PR
LEFT JOIN
    TB_PESQUISADOR_PROJETO PP ON PR.ID_PROJETO = PP.CO_PROJETO
LEFT JOIN
    TB_PUBLICACAO_AUTORIA PA ON PP.CO_PESQUISADOR = PA.CO_PESQUISADOR
GROUP BY
    PR.ID_PROJETO, PR.NM_PROJETO, PR.VL_ORCAMENTO
HAVING
    PR.VL_ORCAMENTO > (SELECT AVG(VL_ORCAMENTO) FROM TB_PROJETO)
    AND COUNT(PA.CO_PUBLICACAO) < (SELECT AVG(PubsPorProjeto) FROM (SELECT COUNT(CO_PUBLICACAO) as PubsPorProjeto FROM TB_PUBLICACAO_AUTORIA GROUP BY CO_PESQUISADOR) as Sub)
ORDER BY
    PR.VL_ORCAMENTO DESC;


-- Consulta 18
-- Requisito Associado: RE3 (Métricas) e Original (Gestão de RH em pesquisa).
-- Descrição: Identifica publicações que resultaram de colaboração inter-laboratorial, ou seja,
--            que possuem autores de pelo menos dois laboratórios distintos.
-- Complexidade: Usa 4 tabelas, SUB-QUERY (CTE), JOIN, COUNT, e GROUP BY.
WITH AutoresLab AS (
    SELECT
        PA.CO_PUBLICACAO,
        PL.CO_LABORATORIO
    FROM
        TB_PUBLICACAO_AUTORIA PA
    JOIN
        TB_PESQUISADOR_LABORATORIO PL ON PA.CO_PESQUISADOR = PL.CO_PESQUISADOR
    GROUP BY
        PA.CO_PUBLICACAO, PL.CO_LABORATORIO
)
SELECT
    P.DS_TITULO,
    COUNT(AL.CO_LABORATORIO) AS "QtdLabsDiferentes"
FROM
    AutoresLab AL
JOIN
    TB_PUBLICACAO P ON AL.CO_PUBLICACAO = P.ID_PUBLICACAO
GROUP BY
    P.DS_TITULO
HAVING
    COUNT(AL.CO_LABORATORIO) >= 2
ORDER BY
    "QtdLabsDiferentes" DESC;


-- Consulta 19
-- Requisito Associado: Original (Recursos financeiros) e RE3 (Métricas).
-- Descrição: Cria um ranking de financiadores com base no "impacto" de suas publicações, dando mais peso
--            para artigos em periódicos de alta reputação (IEEE, Nature, ACM, etc.).
-- Complexidade: Usa 5 tabelas, SUB-QUERY (CTE), JOIN, COUNT, GROUP BY, e WINDOW.
WITH PublicacoesDeImpacto AS (
    SELECT
        FP.CO_FINANCIADOR,
        COUNT(PUB.ID_PUBLICACAO) AS "TotalPublicacoesImpacto"
    FROM
        TB_PUBLICACAO PUB
    JOIN
        TB_PUBLICACAO_AUTORIA PA ON PUB.ID_PUBLICACAO = PA.CO_PUBLICACAO
    JOIN
        TB_PESQUISADOR_PROJETO PP ON PA.CO_PESQUISADOR = PP.CO_PESQUISADOR
    JOIN
        TB_FINANCIADOR_PROJETO FP ON PP.CO_PROJETO = FP.CO_PROJETO
    WHERE
        PUB.NM_EDITORA IN ('IEEE', 'Nature', 'ACM', 'Springer', 'Elsevier')
    GROUP BY
        FP.CO_FINANCIADOR
)
SELECT
    F.NM_FINANCIADOR,
    COALESCE(PI.TotalPublicacoesImpacto, 0) AS "QtdPublicacoesDeImpacto",
    RANK() OVER (ORDER BY COALESCE(PI.TotalPublicacoesImpacto, 0) DESC) AS "RankingDeImpacto"
FROM
    TB_FINANCIADOR F
LEFT JOIN
    PublicacoesDeImpacto PI ON F.ID_FINANCIADOR = PI.CO_FINANCIADOR
WHERE F.ST_REGISTRO_ATIVO = 'S';


-- Consulta 20
-- Requisito Associado: Original (Cadastrar pesquisadores) e RE5 (Avaliar pesquisadores).
-- Descrição: Compara o desempenho médio (nota de avaliação) entre pesquisadores bolsistas e não bolsistas,
--            agrupados por titulação, para analisar se o recebimento de bolsa tem correlação com o desempenho.
-- Complexidade: Usa 3 tabelas, SUB-QUERY, JOIN, COUNT, AVG, e GROUP BY.
SELECT
    P.DS_TITULACAO,
    CASE WHEN P.TP_BOLSA IS NOT NULL THEN 'Bolsista' ELSE 'Não Bolsista' END AS "StatusBolsa",
    COUNT(DISTINCT P.ID_PESQUISADOR) AS "QtdPesquisadores",
    AVG(A.QT_NOTA) AS "NotaMedia"
FROM
    TB_PESQUISADOR P
JOIN
    TB_AVALIACAO A ON P.ID_PESQUISADOR = A.CO_PESQUISADOR
GROUP BY
    P.DS_TITULACAO,
    CASE WHEN P.TP_BOLSA IS NOT NULL THEN 'Bolsista' ELSE 'Não Bolsista' END
ORDER BY
    P.DS_TITULACAO, "StatusBolsa";

