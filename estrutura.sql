-- Requisito Associado: RE1 (Registrar equipamentos e laboratórios) e RE2 (Controlar situação de disponibilidade).
-- Descrição: Esta consulta lista todos os laboratórios, conta o número total de equipamentos em cada um e,
--            separadamente, conta quantos desses equipamentos estão com o status 'Disponível'.
--            Isso ajuda na gestão de alocação de recursos.
-- Complexidade: Usa 3 tabelas (TB_LABORATORIO, TB_EQUIPAMENTO, TB_PESQUISADOR_LABORATORIO) e 3 funções (JOIN, COUNT, GROUP BY).
SELECT
    L.NM_LABORATORIO,
    L.DS_AREA,
    COUNT(E.ID_EQUIPAMENTO) AS "Total de Equipamentos",
    COUNT(CASE WHEN E.ST_DISPONIBILIDADE = 'Disponível' THEN 1 END) AS "Equipamentos Disponíveis"
FROM
    TB_LABORATORIO L
JOIN
    TB_EQUIPAMENTO E ON L.ID_LABORATORIO = E.CO_LABORATORIO
JOIN
    TB_PESQUISADOR_LABORATORIO PL ON L.ID_LABORATORIO = PL.CO_LABORATORIO
WHERE L.ST_REGISTRO_ATIVO = 'S'
GROUP BY
    L.NM_LABORATORIO, L.DS_AREA
ORDER BY
    "Total de Equipamentos" DESC;


-- Consulta 2
-- Requisito Associado: RE3 (Associar publicações a pesquisadores para métricas).
-- Descrição: A consulta gera uma métrica de produtividade, contando o número de publicações por pesquisador,
--            agrupadas por categoria (ex: Artigo de Periódico, Livro).
-- Complexidade: Usa 3 tabelas (TB_PESQUISADOR, TB_PUBLICACAO_AUTORIA, TB_PUBLICACAO) e 3 funções (JOIN, COUNT, GROUP BY).
SELECT
    P.NM_PESQUISADOR,
    PUB.TP_CATEGORIA,
    COUNT(PUB.ID_PUBLICACAO) AS "Quantidade de Publicações"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PUBLICACAO_AUTORIA PA ON P.ID_PESQUISADOR = PA.CO_PESQUISADOR
JOIN
    TB_PUBLICACAO PUB ON PA.CO_PUBLICACAO = PUB.ID_PUBLICACAO
GROUP BY
    P.NM_PESQUISADOR,
    PUB.TP_CATEGORIA
ORDER BY
    P.NM_PESQUISADOR,
    "Quantidade de Publicações" DESC;


-- Consulta 3
-- Requisito Associado: RE4 (Gerar relatórios de participação em eventos).
-- Descrição: Esta consulta produz um relatório que totaliza a participação de cada pesquisador em eventos,
--            agrupando por tipo de evento (Conferência, Workshop, etc.).
-- Complexidade: Usa 3 tabelas (TB_PESQUISADOR, TB_PESQUISADOR_EVENTO, TB_EVENTO) e 3 funções (JOIN, COUNT, GROUP BY).
SELECT
    P.NM_PESQUISADOR,
    E.TP_EVENTO,
    COUNT(E.ID_EVENTO) AS "Total de Eventos Participados"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PESQUISADOR_EVENTO PE ON P.ID_PESQUISADOR = PE.CO_PESQUISADOR
JOIN
    TB_EVENTO E ON PE.CO_EVENTO = E.ID_EVENTO
GROUP BY
    P.NM_PESQUISADOR,
    E.TP_EVENTO
ORDER BY
    P.NM_PESQUISADOR,
    "Total de Eventos Participados" DESC;


-- Consulta 4
-- Requisito Associado: RE5 (Avaliar pesquisadores em escala) e RE6 (Registrar feedback).
-- Descrição: Calcula a nota média de avaliação de cada pesquisador dentro de um projeto específico,
--            e também conta quantas avaliações foram feitas, permitindo uma análise de desempenho.
-- Complexidade: Usa 3 tabelas (TB_AVALIACAO, TB_PESQUISADOR, TB_PROJETO) e 4 funções (JOIN, AVG, COUNT, GROUP BY).
SELECT
    PR.NM_PROJETO,
    P.NM_PESQUISADOR,
    AVG(A.QT_NOTA) AS "Nota Média",
    COUNT(A.ID_AVALIACAO) AS "Número de Avaliações"
FROM
    TB_AVALIACAO A
JOIN
    TB_PESQUISADOR P ON A.CO_PESQUISADOR = P.ID_PESQUISADOR
JOIN
    TB_PROJETO PR ON A.CO_PROJETO = PR.ID_PROJETO
GROUP BY
    PR.NM_PROJETO,
    P.NM_PESQUISADOR
ORDER BY
    PR.NM_PROJETO,
    "Nota Média" DESC;


-- Consulta 5
-- Requisito Associado: Requisito Original (Gerenciar distribuição de recursos financeiros).
-- Descrição: Lista o orçamento total e a quantidade de projetos gerenciados por cada financiador,
--            dando uma visão clara da distribuição de verbas.
-- Complexidade: Usa 3 tabelas (TB_FINANCIADOR, TB_FINANCIADOR_PROJETO, TB_PROJETO) e 4 funções (JOIN, SUM, COUNT, GROUP BY).
SELECT
    F.NM_FINANCIADOR,
    COUNT(P.ID_PROJETO) AS "Quantidade de Projetos Financiados",
    SUM(P.VL_ORCAMENTO) AS "Orçamento Total Gerenciado"
FROM
    TB_FINANCIADOR F
JOIN
    TB_FINANCIADOR_PROJETO FP ON F.ID_FINANCIADOR = FP.CO_FINANCIADOR
JOIN
    TB_PROJETO P ON FP.CO_PROJETO = P.ID_PROJETO
WHERE F.ST_REGISTRO_ATIVO = 'S'
GROUP BY
    F.NM_FINANCIADOR
ORDER BY
    "Orçamento Total Gerenciado" DESC;


-- Consulta 6
-- Requisito Associado: RE3 (Métricas de divulgação científica).
-- Descrição: Cria um ranking de pesquisadores com base no número de publicações feitas desde o início do ano passado,
--            utilizando uma função de janela para classificar.
-- Complexidade: Usa 3 tabelas (TB_PESQUISADOR, TB_PUBLICACAO_AUTORIA, TB_PUBLICACAO) e 4 funções (JOIN, COUNT, GROUP BY, WINDOW).
SELECT
    P.NM_PESQUISADOR,
    COUNT(PUB.ID_PUBLICACAO) AS "Publicações Recentes",
    RANK() OVER (ORDER BY COUNT(PUB.ID_PUBLICACAO) DESC) AS "Ranking"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PUBLICACAO_AUTORIA PA ON P.ID_PESQUISADOR = PA.CO_PESQUISADOR
JOIN
    TB_PUBLICACAO PUB ON PA.CO_PUBLICACAO = PUB.ID_PUBLICACAO
WHERE
    PUB.DT_PUBLICACAO >= '2023-01-01'
GROUP BY
    P.NM_PESQUISADOR
ORDER BY
    "Ranking";


-- Consulta 7
-- Requisito Associado: RE5 (Avaliar pesquisadores).
-- Descrição: Compara a nota de cada avaliação individual de um pesquisador com a nota média geral do projeto
--            correspondente, ajudando a identificar desempenhos acima ou abaixo da média do grupo.
-- Complexidade: Usa 3 tabelas (TB_AVALIACAO, TB_PESQUISADOR, TB_PROJETO) e 2 funções (JOIN, WINDOW).
SELECT
    P.NM_PESQUISADOR,
    PR.NM_PROJETO,
    A.QT_NOTA AS "Nota Individual",
    A.DT_AVALIACAO,
    AVG(A.QT_NOTA) OVER (PARTITION BY A.CO_PROJETO) AS "Média do Projeto"
FROM
    TB_AVALIACAO A
JOIN
    TB_PESQUISADOR P ON A.CO_PESQUISADOR = P.ID_PESQUISADOR
JOIN
    TB_PROJETO PR ON A.CO_PROJETO = PR.ID_PROJETO
ORDER BY
    P.NM_PESQUISADOR, A.DT_AVALIACAO;


-- Consulta 8
-- Requisito Associado: Requisito Original (Monitorar a execução de projetos de pesquisa).
-- Descrição: Conta quantos pesquisadores de cada titulação (Doutorado, Mestrado, etc.) estão
--            trabalhando em cada projeto ativo.
-- Complexidade: Usa 3 tabelas (TB_PESQUISADOR, TB_PESQUISADOR_PROJETO, TB_PROJETO) e 3 funções (JOIN, COUNT, GROUP BY).
SELECT
    PR.NM_PROJETO,
    P.DS_TITULACAO,
    COUNT(P.ID_PESQUISADOR) AS "Quantidade de Pesquisadores"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PESQUISADOR_PROJETO PP ON P.ID_PESQUISADOR = PP.CO_PESQUISADOR
JOIN
    TB_PROJETO PR ON PP.CO_PROJETO = PR.ID_PROJETO
WHERE PR.ST_PROJETO = 'A'
GROUP BY
    PR.NM_PROJETO,
    P.DS_TITULACAO
ORDER BY
    PR.NM_PROJETO,
    P.DS_TITULACAO;


-- Consulta 9
-- Requisito Associado: RE1 (Registrar equipamentos) e Requisito Original (Cadastrar pesquisadores).
-- Descrição: Mostra a quantidade de pesquisadores por laboratório e a quantidade de equipamentos
--            em manutenção nesses mesmos laboratórios, ajudando a correlacionar a necessidade de
--            recursos com a equipe alocada.
-- Complexidade: Usa 4 tabelas (TB_LABORATORIO, TB_PESQUISADOR_LABORATORIO, TB_PESQUISADOR, TB_EQUIPAMENTO) e 3 funções (JOIN, COUNT, GROUP BY).
SELECT
    L.NM_LABORATORIO,
    COUNT(DISTINCT P.ID_PESQUISADOR) AS "Número de Pesquisadores",
    COUNT(CASE WHEN E.ST_DISPONIBILIDADE = 'Manutenção' THEN 1 END) AS "Equipamentos em Manutenção"
FROM
    TB_LABORATORIO L
LEFT JOIN
    TB_PESQUISADOR_LABORATORIO PL ON L.ID_LABORATORIO = PL.CO_LABORATORIO
LEFT JOIN
    TB_PESQUISADOR P ON PL.CO_PESQUISADOR = P.ID_PESQUISADOR
LEFT JOIN
    TB_EQUIPAMENTO E ON L.ID_LABORATORIO = E.CO_LABORATORIO
GROUP BY
    L.NM_LABORATORIO
ORDER BY
    "Número de Pesquisadores" DESC;


-- Consulta 10
-- Requisito Associado: RE4 (Relatórios de participação em eventos).
-- Descrição: Calcula o total de horas de participação em eventos por pesquisador, somando a carga horária
--            de todos os eventos que cada um participou.
-- Complexidade: Usa 3 tabelas (TB_PESQUISADOR, TB_PESQUISADOR_EVENTO, TB_EVENTO) e 3 funções (JOIN, SUM, GROUP BY).
SELECT
    P.NM_PESQUISADOR,
    SUM(E.QT_CARGA_HORARIA) AS "Total Horas em Eventos"
FROM
    TB_PESQUISADOR P
JOIN
    TB_PESQUISADOR_EVENTO PE ON P.ID_PESQUISADOR = PE.CO_PESQUISADOR
JOIN
    TB_EVENTO E ON PE.CO_EVENTO = E.ID_EVENTO
GROUP BY
    P.NM_PESQUISADOR
HAVING
    SUM(E.QT_CARGA_HORARIA) > 0
ORDER BY
    "Total Horas em Eventos" DESC;

